  def initialize(sender, sheet_manager)
    @sender        = sender.to_s.gsub('@', '')
    @sheet_manager = sheet_manager
  end

  def execute
    user = @sheet_manager.find_user(@sender)
    return "@#{@sender} 등록되지 않은 계정입니다." unless user

    card_name, reading = TAROT_DATA.to_a.sample

    today = Time.now.strftime('%Y-%m-%d')
    @sheet_manager.update_user(@sender, { last_tarot_date: today })

    "@#{@sender} 카드를 한 장 뽑았습니다.\n\n【 #{card_name} 】\n\n#{reading}"
  end
